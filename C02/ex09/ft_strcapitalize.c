#include<stdio.h>

char	*ft_strcapitalize(char *str)
{
	if('a' <= str[0] && str[0] <= 'z')
		str[0] = str[0] - 32;

	int i;
	i = 1;
	while(str[i] != '\0')
	{
		if('A' <= str[i] && str[i] <= 'Z')
			str[i] = str[i] + 32;
	}
}


